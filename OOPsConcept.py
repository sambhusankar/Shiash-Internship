# Incapsulation 
class employee:
    def __phonenumber():
        print(8238187630)
    def show(self):
        employee.__phonenumber()


E = employee()
E.show()


# Abstraction
class student:
    def username(self):
        print("deepak")

    def password(self):
        pass

class deepak(student):
    def password(self):
        print("545415445")

D = deepak()
D.username()
D.password()